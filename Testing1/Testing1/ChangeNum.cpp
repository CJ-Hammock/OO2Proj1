#include <iostream>
#include "ChangeNum.h"

using namespace std;

int DoubleNum(int num)
{
	return num * 2;
}

int IncrementUp(int num)
{
	return num+1;
}

int DecrementDown(int num)
{
	return num-1;
}

int Product(int num1, int num2)
{
	return num1*num2;
}

int Sum(int num1, int num2)
{
	return num1 + num2;
}